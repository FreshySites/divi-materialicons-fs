/**
 * Overwrites the same function in Divi's functions.php file. 
 * Identify the new icons in the divi builder so we can handle 
 * them accordingly.
 *
 * @since    1.0.0
 */
if ( ! function_exists( 'et_pb_get_font_icon_list_items' ) ) {
	
	function et_pb_get_font_icon_list_items() {

		$output = '';
	
		$symbols = et_pb_get_font_icon_symbols();
			
		foreach ( $symbols as $symbol ) {
		
			$icon_checker = substr( $symbol, 0, 3 );

			// if Divi Font Awesome plugin is in use, still render those icons
			if( $icon_checker === 'fa-' ) :
				$symbol_parts = explode( '-', $symbol );
				$output .= sprintf( '<li data-icon=\'%1$s\' class="fa fa-fw fa-divi-fontawesome-builder"></li>', esc_attr( $symbol_parts[1] ) );
			// and of course, for our Material Icons, add the appropriate classes too
			elseif( $icon_checker === 'mi-' ) :
				$symbol_parts = explode( '-', $symbol );
				$output .= sprintf( '<li data-icon=\'%1$s\' class="mi mi-divi-materialicons-builder"></li>', esc_attr( $symbol_parts[1] ) );
			// otherwise, do the normal Divi output of the list item wrapper
			else :
				$output .= sprintf( '<li data-icon=\'%1$s\'></li>', esc_attr( $symbol ) );
			endif;
		}

		return $output;
	}
}

