/**
 * Filter plugin action links.
 *
 * @since    1.0.0
 */
function fs_dmi_add_action_plugin( $actions, $plugin_file ) {
	static $plugin;

	if ( ! isset( $plugin ) )
		$plugin = plugin_basename(__FILE__);
	
	if ($plugin == $plugin_file) :

		$settings = array('settings' => '<a href="options-general.php?page=' . FS_DMI_SETTINGS . '">' . __('Settings', 'General') . '</a>');
		//$site_link = array('support' => '<a href="https://freshysites.com" target="_blank">Support</a>');
		
    	$actions = array_merge($settings, $actions);
		//$actions = array_merge($site_link, $actions);
			
	endif;
		
	return $actions;
}

